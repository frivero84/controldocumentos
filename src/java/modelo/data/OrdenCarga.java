package modelo.data;

/**
 *
 * @author nando
 */
public class OrdenCarga {
    private int ukid;
    private String cedula;
    private String fecha_carga;
    private String tipo_carga;
    private String lugar_carga;
    private String destino_carga;
    private String usuario;
    private String fecha;
    private String hora;

    /**
     * @return the ukid
     */
    public int getUkid() {
        return ukid;
    }

    /**
     * @param ukid the ukid to set
     */
    public void setUkid(int ukid) {
        this.ukid = ukid;
    }

    /**
     * @return the cedula
     */
    public String getCedula() {
        return cedula;
    }

    /**
     * @param cedula the cedula to set
     */
    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    /**
     * @return the fecha_carga
     */
    public String getFecha_carga() {
        return fecha_carga;
    }

    /**
     * @param fecha_carga the fecha_carga to set
     */
    public void setFecha_carga(String fecha_carga) {
        this.fecha_carga = fecha_carga;
    }

    /**
     * @return the tipo_carga
     */
    public String getTipo_carga() {
        return tipo_carga;
    }

    /**
     * @param tipo_carga the tipo_carga to set
     */
    public void setTipo_carga(String tipo_carga) {
        this.tipo_carga = tipo_carga;
    }

    /**
     * @return the lugar_carga
     */
    public String getLugar_carga() {
        return lugar_carga;
    }

    /**
     * @param lugar_carga the lugar_carga to set
     */
    public void setLugar_carga(String lugar_carga) {
        this.lugar_carga = lugar_carga;
    }

    /**
     * @return the destino_carga
     */
    public String getDestino_carga() {
        return destino_carga;
    }

    /**
     * @param destino_carga the destino_carga to set
     */
    public void setDestino_carga(String destino_carga) {
        this.destino_carga = destino_carga;
    }

    /**
     * @return the usuario
     */
    public String getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the hora
     */
    public String getHora() {
        return hora;
    }

    /**
     * @param hora the hora to set
     */
    public void setHora(String hora) {
        this.hora = hora;
    }


}
