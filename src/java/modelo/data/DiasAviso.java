public class DiasAviso {

    private int id_documento;
    private int dias_aviso;

    /**
     * @return the id_documento
     */
    public int getId_documento() {
        return id_documento;
    }

    /**
     * @param id_documento the id_documento to set
     */
    public void setId_documento(int id_documento) {
        this.id_documento = id_documento;
    }

    /**
     * @return the dias_aviso
     */
    public int getDias_aviso() {
        return dias_aviso;
    }

    /**
     * @param dias_aviso the dias_aviso to set
     */
    public void setDias_aviso(int dias_aviso) {
        this.dias_aviso = dias_aviso;
    }
}
